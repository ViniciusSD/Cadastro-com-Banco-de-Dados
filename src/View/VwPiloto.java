    private void jButtonEditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonEditMouseClicked
        // TODO add your handling code here:
        Piloto pil = MontaPiloto();
        int temp = pil.getId();
        if (ctrl.Alterar(pil)) {
            JOptionPane.showMessageDialog(this, "Piloto alterado com sucesso.");
            jButtonAbrirMouseClicked(evt);
            Buscar(temp);
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao alterar piloto.");
        }
    }//GEN-LAST:event_jButtonEditMouseClicked

    public void Nav(Piloto piloto) {
        jTextFieldPiloto.setText(piloto.getNome());
        jTextFieldNac.setText(piloto.getNacionalidade());
        jTextFieldEquipe.setText(piloto.getEquipe());
        jTextFieldNum.setText(String.valueOf(piloto.getNumero()));
        jTextFieldPts.setText(String.valueOf(piloto.getPontos()));
        jTextFieldId.setText(String.valueOf(piloto.getId()));
    }

    public void Buscar(int index) {
        Piloto piloto = ctrl.getPilotoPorId(index);
        Nav(piloto);
    }

    public Piloto MontaPiloto() {
        Piloto pil = null;
        int temp;
        if (jTextFieldId.getText().equals("")) {
            temp = 0;
            System.out.println("id virou 0");
        } else {
            temp = Integer.valueOf(jTextFieldId.getText());
            System.out.println("id virou: " + temp);
        }
        int id = temp;
        String nome = jTextFieldPiloto.getText();
        String nacionalidade = jTextFieldNac.getText();
        String equipe = jTextFieldEquipe.getText();
        int numero = Integer.valueOf(jTextFieldNum.getText());
        int pontos = Integer.valueOf(jTextFieldPts.getText());
        pil = new Piloto(id, nome, nacionalidade, equipe, numero, pontos);
        return pil;
    }

    public void LimpaCampos() {
        jTextFieldId.setText("");
        jTextFieldPiloto.setText("");
        jTextFieldNac.setText("");
        jTextFieldEquipe.setText("");
        jTextFieldNum.setText("");
        jTextFieldPts.setText("");
    }

