    private void jButtonBuscaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonBuscaMouseClicked
        // TODO add your handling code here
        try {
            int aux = Integer.parseInt(jTextFieldId.getText());
            Piloto piloto = ctrl.getPilotoPorId(aux);
            if (piloto == null) {
                JOptionPane.showMessageDialog(this, "Informe um numero valido");
                return;
            }
            Nav(piloto);
        } catch (NumberFormatException erro) {
            System.out.println(erro);
            JOptionPane.showMessageDialog(this, "Informe um numero valido");
        }
    }//GEN-LAST:event_jButtonBuscaMouseClicked

    private void jButtonDelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonDelMouseClicked
        // TODO add your handling code here:
        try {
            Piloto pil = MontaPiloto();
            System.out.println("Piloto: " + pil.getId() + pil.getNome() + pil.getEquipe());
            System.out.println("Id: " + pil.getId());
            if (ctrl.Excluir(pil)) {
                JOptionPane.showMessageDialog(this, "Piloto excluido com sucesso. \nReiniciando a navegacao.");
                jButtonAbrirMouseClicked(evt);
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir piloto.");
            }
        } catch (NumberFormatException erro) {
            System.out.println(erro);
            JOptionPane.showMessageDialog(this, "Informe um ID valido.");
        }
    }//GEN-LAST:event_jButtonDelMouseClicked

