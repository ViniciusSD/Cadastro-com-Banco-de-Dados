    private void jButtonSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonSalvarMouseClicked
        // TODO add your handling code here:
        Piloto pil = MontaPiloto();
        pil.setId(ctrl.getUltimoId() + 1);
        if (ctrl.Inserir(pil)) {
            JOptionPane.showMessageDialog(this, "Piloto inserido com sucesso.");
            jButtonAbrirMouseClicked(evt);
            if (JDBCUtil.MovUltimo(ctrl.getRsdados())) {
                Nav(ctrl.getPiloto());
            } else {
                JOptionPane.showMessageDialog(this, "O ultimo registro ja esta selecionado.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao inserir piloto.");
        }
    }//GEN-LAST:event_jButtonSalvarMouseClicked

    private void jButtonAbrirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonAbrirMouseClicked
        // TODO add your handling code here:
        if (ctrl.CriaConexao()) {
            if (ctrl.ConsultarTodos()) {
                if (JDBCUtil.MovPrimeiro(ctrl.getRsdados())) {
                    Piloto piloto = ctrl.getPiloto();
                    if (piloto != null) {
                        Nav(piloto);
                    } else {
                        JOptionPane.showMessageDialog(this, "Piloto retornado é nulo.");
                        LimpaCampos();
                    }
                }
            }
        }
    }//GEN-LAST:event_jButtonAbrirMouseClicked

